async function upsertJournal(db: AnyPrisma, data: any, lines: Array<{ accountCode: string; debit?: number; credit?: number; description: string }>) {
  const accounts = await db.ledgerAccount.findMany({ where: { code: { in: lines.map((line) => line.accountCode) } } });
  const accountByCode = new Map(accounts.map((account: any) => [account.code, account]));
  const totalDebit = lines.reduce((sum, line) => sum + (line.debit ?? 0), 0);
  const totalCredit = lines.reduce((sum, line) => sum + (line.credit ?? 0), 0);

  if (Math.round(totalDebit * 100) !== Math.round(totalCredit * 100)) {
    throw new Error(`Seed journal ${data.entryNumber} is unbalanced`);
  }

  const entry = await db.journalEntry.upsert({
    where: { entryNumber: data.entryNumber },
    update: {
      entryDate: data.entryDate,
      postingDate: data.postingDate ?? data.entryDate,
      description: data.description,
      reference: data.reference,
      entryType: data.entryType,
      status: 'POSTED',
      totalDebit: money(totalDebit),
      totalCredit: money(totalCredit),
      paymentId: data.paymentId ?? null,
      policyId: data.policyId ?? null,
      insurerId: data.insurerId ?? null,
      clientId: data.clientId ?? null,
      commissionEntryId: data.commissionEntryId ?? null,
      remittanceId: data.remittanceId ?? null,
      expenseId: data.expenseId ?? null,
      agentId: data.agentId ?? null,
      approvedById: data.userId,
      approvedAt: data.postingDate ?? data.entryDate,
      postedById: data.userId,
      postedAt: data.postingDate ?? data.entryDate,
      postingEvent: data.postingEvent ?? null,
      notes: `${SEED_TAG} ${data.description}`,
      sourceKey: data.sourceKey,
      createdById: data.userId,
    },
    create: {
      id: data.id,
      entryNumber: data.entryNumber,
      entryDate: data.entryDate,
      postingDate: data.postingDate ?? data.entryDate,
      description: data.description,
      reference: data.reference,
      entryType: data.entryType,
      status: 'POSTED',
      totalDebit: money(totalDebit),
      totalCredit: money(totalCredit),
      paymentId: data.paymentId ?? null,
      policyId: data.policyId ?? null,
      insurerId: data.insurerId ?? null,
      clientId: data.clientId ?? null,
      commissionEntryId: data.commissionEntryId ?? null,
      remittanceId: data.remittanceId ?? null,
      expenseId: data.expenseId ?? null,
      agentId: data.agentId ?? null,
      approvedById: data.userId,
      approvedAt: data.postingDate ?? data.entryDate,
      postedById: data.userId,
      postedAt: data.postingDate ?? data.entryDate,
      postingEvent: data.postingEvent ?? null,
      notes: `${SEED_TAG} ${data.description}`,
      sourceKey: data.sourceKey,
      createdById: data.userId,
    },
  });

  await db.journalEntryLine.deleteMany({ where: { journalEntryId: entry.id } });
  for (const [idx, line] of lines.entries()) {
    const account = accountByCode.get(line.accountCode);
    if (!account) throw new Error(`Missing ledger account ${line.accountCode}`);
    await db.journalEntryLine.create({
      data: {
        id: sampleId(`journal-line-${data.entryNumber}-${idx + 1}`),
        journalEntryId: entry.id,
        accountId: account.id,
        debit: money(line.debit ?? 0),
        credit: money(line.credit ?? 0),
        description: line.description,
        referenceType: data.referenceType ?? null,
        referenceId: data.referenceId ?? null,
      },
    });
  }
  return entry;
}

