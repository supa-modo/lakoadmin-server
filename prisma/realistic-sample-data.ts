const SEED_TAG = '[SEED:realistic-v2]';

type AnyPrisma = any;

const dayMs = 24 * 60 * 60 * 1000;

function sampleId(name: string): string {
  const h = createHash('sha256').update(`lako-agency:sample:${name}`).digest();
  const b = Buffer.allocUnsafe(16);
  h.copy(b, 0, 0, 16);
  b[6] = (b[6]! & 0x0f) | 0x40;
  b[8] = (b[8]! & 0x3f) | 0x80;
  const hex = b.toString('hex');
  return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-${hex.slice(12, 16)}-${hex.slice(16, 20)}-${hex.slice(20, 32)}`;
}

function daysFrom(anchor: Date, days: number): Date {
  return new Date(anchor.getTime() + days * dayMs);
}

function money(value: number): string {
  return value.toFixed(2);
}

function hashFor(value: string): string {
  return createHash('sha256').update(value).digest('hex');
}

function clientDisplayName(client: any): string {
  return client.companyName ?? `${client.firstName ?? ''} ${client.lastName ?? ''}`.trim();
}

